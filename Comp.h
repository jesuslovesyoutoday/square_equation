#include <stdio.h>

//----------------------------------------------------------------------------
//! Compares two arguments
//! 
//! @param[in] a first argument
//! @param[in] b second param
//! 
//! @return outcome of comparison
//!
//! @note	if function returns '0' - it means that arguments are the same
//! 		if function returns '-1' - it means that first argument is smaller
